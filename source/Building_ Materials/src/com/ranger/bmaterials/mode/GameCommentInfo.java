package com.ranger.bmaterials.mode;

public class GameCommentInfo {
	private String commentid;
	private String userid;
	private String cmtusername;
	private String commenttime;
	private String cmtcotent;
	public String getCommentid() {
		return commentid;
	}
	public void setCommentid(String commentid) {
		this.commentid = commentid;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getCmtusername() {
		return cmtusername;
	}
	public void setCmtusername(String cmtusername) {
		this.cmtusername = cmtusername;
	}
	public String getCommenttime() {
		return commenttime;
	}
	public void setCommenttime(String commenttime) {
		this.commenttime = commenttime;
	}
	public String getCmtcotent() {
		return cmtcotent;
	}
	public void setCmtcotent(String cmtcotent) {
		this.cmtcotent = cmtcotent;
	}
	
	
	
}
